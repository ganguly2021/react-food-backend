const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// create recipe schema
const RecipeSchema = Schema({
  name: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  instructions: {
    type: String,
    required: true
  },
  createdDate: {
    type: Date,
    default: Date.now
  },
  likes: {
    type: Number,
    default: 0
  },
  username: {
    type: String
  }
});

// export recipe schema
module.exports = mongoose.model('recipes', RecipeSchema);